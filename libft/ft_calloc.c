#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	size_t	*p;

	p = malloc(nmemb * size);
	if (p == NULL)
		return (NULL);
	ft_bzero(p, nmemb * size);
	return (p);
}
